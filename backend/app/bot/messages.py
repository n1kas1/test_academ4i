def msg_balance_free(used: int, is_admin: bool = False) -> str:
    if is_admin:
        return "👑 <b>Админ-аккаунт</b>."
    left = max(0, settings.free_daily_cap - used)
    return (
        f"Сегодня решено: <b>{used}</b>\n"
        f"Доступно ещё: <b>{left}</b> задач\n\n"
        "Обновление — в 00:00 МСК."
    )


