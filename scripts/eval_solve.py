"""Eval-харнесс «бот решает ВСЁ»: прогон разнотипных задач (простые/сложные/
теоретические × все темы) через настоящие гейт+солвер. Ловит ложные отказы
(гейт verdict=NO ИЛИ строка-отказ солвера) — регрессия после правок промптов/гейта.

Запуск (нужен API-ключ → только в Docker):
    docker compose run --rm backend python scripts/eval_solve.py
"""
import asyncio

from app.ai.haiku_gate import is_math_or_physics
from app.ai.gemini import solve_with_gemini

TASKS = [
    # простые / голые выражения (частый источник ложных отказов)
    "2+2*2", "5!", "НОД(48,36)", "x^2-5x+6=0", "√144 + 2^5",
    # матан
    "найди производную x^3 * ln x", "вычисли определённый интеграл от 0 до 1 от x^2 dx",
    "lim x->0 (1-cos x)/x^2", "исследуй функцию y=x^3-3x и построй график",
    # линал
    "определитель матрицы [[1,2],[3,4]]", "реши систему x+y=5, 2x-y=1",
    "собственные значения матрицы [[2,0],[0,3]]",
    # алгебра / группы (теоретические)
    "докажи что подгруппа индекса 2 нормальна", "порядок элемента 3 в группе Z_12",
    # тервер
    "вероятность суммы 7 при броске двух кубиков", "матожидание биномиального распределения B(10, 0.3)",
    "построй график плотности показательного распределения с параметром 2",
    # дискретка
    "число сочетаний C(8,3)", "таблица истинности (x and y) or (not z)",
    "является ли граф K5 эйлеровым", "реши рекуррентное соотношение a_n=2a_{n-1}+1, a_0=1",
    # физика
    "тело брошено под углом 30 градусов со скоростью 20 м/с, найди дальность",
    "сила тока через резистор R=10 Ом при напряжении U=5 В", "период математического маятника длиной 1 м",
    # теоретические
    "сформулируй теорему Лагранжа о конечных приращениях", "докажи иррациональность корня из 2",
    "что такое предел последовательности",
]

_REFUSAL = "В присланном сообщении нет математическо"
_sem = asyncio.Semaphore(5)


async def _check(t: str) -> tuple[str, str]:
    async with _sem:
        try:
            if not await is_math_or_physics(t):
                return (t, "GATE=NO")
            out = await solve_with_gemini(t)
            return (t, "REFUSE") if out.strip().startswith(_REFUSAL) else (t, "OK")
        except Exception as e:
            return (t, f"ERR:{e}")


async def main() -> None:
    res = await asyncio.gather(*[_check(t) for t in TASKS])
    bad = [r for r in res if r[1] != "OK"]
    print(f"ВСЕГО {len(res)} | OK {len(res) - len(bad)} | ПРОБЛЕМ {len(bad)}")
    for t, s in bad:
        print(f"  [{s}] {t}")
    print("ВСЕ ЗАДАЧИ РЕШЕНЫ ✓" if not bad else "ЕСТЬ ЛОЖНЫЕ ОТКАЗЫ — чинить промпт/гейт")


if __name__ == "__main__":
    asyncio.run(main())
